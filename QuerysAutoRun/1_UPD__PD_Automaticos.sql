USE COLNOTEXSA
UPDATE 
	TRADE
SET 
	AUTORIZA = '1'
WHERE (TIPODCTO = 'PD')
	AND (ORIGEN = 'FAC')
	AND (AUTORIZA = '0')
	AND (FECHA >= '01/01/2025')
	AND (TIPOVTA IN ('0-050', '0-051', '0-055', '0-056', '0-058', '0-059', '0-063', '0-070', '0-071'))
    AND PASSWORDIN='SUPER'

PRINT 'Ejecucion realizada en: ' + CONVERT(VARCHAR, GETDATE(), 120);
